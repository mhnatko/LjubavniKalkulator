    private void btnRezultatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRezultatActionPerformed
    
    String s1 = txtPrvaOsoba.getText();
    String s2 = txtDrugaOsoba.getText();
    
    int sum1=0,sum2=0;    
    for(int i=0;i<s1.length();i++){
        char ch = s1.charAt(i);
        int ascii = ch;
        sum1=sum1+ascii;
    }    
    for(int i=0;i<s2.length();i++){
        char ch = s2.charAt(i);
        int ascii = ch;
        sum2=sum2+ascii;
    }  
    
    int ukupno = sum1 + sum2;
    int ljubavPostotak = ukupno%100;
    txtRezultat.setText(ljubavPostotak + "%");
    }//GEN-LAST:event_btnRezultatActionPerformed

